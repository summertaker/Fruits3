package com.summertaker.fruits3;

public class Member {
    private String id;
    private String group;
    private String team;
    private String name;
    private String furigana;
    private String birthday;
    private String age;
    private String image;
    private String twitter;
    private String instagram;
    private String wiki;

    Member() {

    }

    public String getId() {
        return id;
    }

    void setId(String id) {
        this.id = id;
    }

    String getGroup() {
        return group;
    }

    void setGroup(String group) {
        this.group = group;
    }

    String getTeam() {
        return team;
    }

    void setTeam(String team) {
        this.team = team;
    }

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    String getFurigana() {
        return furigana;
    }

    void setFurigana(String furigana) {
        this.furigana = furigana;
    }

    String getBirthday() {
        return birthday;
    }

    void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    String getAge() {
        return age;
    }

    void setAge(String age) {
        this.age = age;
    }

    String getImage() {
        return image;
    }

    void setImage(String image) {
        this.image = image;
    }

    String getTwitter() {
        return twitter;
    }

    void setTwitter(String twitter) {
        this.twitter = twitter;
    }

    String getInstagram() {
        return instagram;
    }

    void setInstagram(String instagram) {
        this.instagram = instagram;
    }

    String getWiki() {
        return wiki;
    }

    void setWiki(String wiki) {
        this.wiki = wiki;
    }
}
